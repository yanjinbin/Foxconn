class NestedIterator {
private:
  deque<int> dq_;

public:
  NestedIterator(vector<NestedInteger> &nestedList) { flatQueue(nestedList); }

  int next() {
    int ans = dq_.front();
    dq_.pop_front();
    return ans;
  }

  bool hasNext() { return !dq_.empty(); }

  void flatQueue(vector<NestedInteger> &nestedList) {
    for (auto em : nestedList) {
      if (em.isInteger())
        dq_.push_back(em.getInteger());
      else
        flatQueue(em.getList());
    }
  }
};

